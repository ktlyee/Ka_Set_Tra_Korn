import 'package:flutter/material.dart';

abstract class CollectionColors {
  static Color lightbrown() {
    return const Color(0xFFF2B950);
  }

  static Color mediumbrown() {
    return const Color(0xFFD99441);
  }
  
  static Color darkbrown() {
    return const Color(0xFFBF6C3B);
  }

  static Color lightgreen() {
    return const Color(0xFFC4D923);
  }
  
  static Color darkgreen() {
    return const Color(0xFF86BF0B);
  }

  static Color gray() {
    return const Color.fromRGBO(224, 224, 224, 0.5);
  }
}